    @Override
    public void update(Observable obs, Object obj){
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("ProfesiÃ³n: " + cliDB.getProfesion());
        this.cHombres = cliDB.getTotalHombres();
        this.cMujeres = cliDB.getTotalMujeres();
        this.lbCantH.setText(cHombres + "");
        this.lbCantM.setText(cMujeres + "");
        this.repaint();
    }
    
    private int mayorValor(int cantHombres, int cantMujeres){
        if(cantHombres >= cantMujeres){
            return cantHombres;
        }
        return cantMujeres;
    }
    @Override
    public void paint(Graphics g){
        super.paint(g);
        
        int mayor = mayorValor(cHombres, cMujeres);

        int largoHombres = cHombres * 250 / mayor;
        int largoMujeres = cMujeres * 250 / mayor;

        g.setColor(new Color(0, 200, 255));
        g.fillRect(40, 50, 50, largoHombres);
        
        g.setColor(new Color(220, 0, 255));
        g.fillRect(140, 50, 50, largoMujeres);
    }
